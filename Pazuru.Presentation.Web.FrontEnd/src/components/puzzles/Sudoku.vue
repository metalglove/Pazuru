<script lang="ts">
import { Component, Vue, Prop } from 'vue-property-decorator';
import { SudokuViewModel, Cell } from '@/viewmodels/SudokuViewModel';
import SudokuCell from './SudokuCell.vue';

@Component({
    components: {
        SudokuCell
    }
})
export default class Sudoku extends Vue {
    @Prop() private sudokuViewModel!: SudokuViewModel;
    private counter: number = 0;

    private getSudokuCell(row: number, column: number): Cell {
        const irow: number = row - 1;
        const icolumn: number = column - 1;
        const cell: Cell = this.sudokuViewModel.puzzleState[irow * icolumn];
        // console.log(cell, irow, icolumn);
        return cell;
    }
}
